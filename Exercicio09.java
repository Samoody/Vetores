public class Exercicio09 {
    public static void main(String[] args) {
        char[] vetor = {'A', 'B', 'C', 'D', 'E', 'F', 'G', 'H'};

        System.out.println("Vetor na ordem inversa:");
        for (int i = vetor.length - 1; i >= 0; i--) {
            System.out.println(vetor[i]);
        }
    }
}
